# 순환 import 회피용 — 카테고리 매핑을 자체 보관
from lucky_items import CARD_CATEGORIES


def _get_category(card_key):
    """card_key (예: 'major_00_upright') → 카테고리"""
    base = card_key.replace('_upright', '').replace('_reversed', '')
    return CARD_CATEGORIES.get(base, '안정/풍요')


def _get_direction(card_key):
    """card_key → 'upright' or 'reversed'"""
    if card_key.endswith('_reversed'):
        return 'reversed'
    return 'upright'


