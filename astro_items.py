def get_energy_map(card_key, date_obj):
    """
    카드 카테고리 기반 4영역 에너지 지도.
    
    [출력]
      [
        ['영역', '에너지', '오늘의 메시지'],
        ['일·학업', '★★★★☆', '...'],
        ['관계',    '★★★☆☆', '...'],
        ['금전',    '★★★☆☆', '...'],
        ['건강',    '★★★★☆', '...'],
      ]
    """
    category = _get_category(card_key)
    em = ENERGY_MAP_BY_CATEGORY.get(category, ENERGY_MAP_BY_CATEGORY['안정/풍요'])
    
    def stars(n):
        return '★' * n + '☆' * (5 - n)
    
    rows = [['영역', '에너지', '오늘의 메시지']]
    for area in ['일·학업', '관계', '금전', '건강']:
        score, msg = em[area]
        rows.append([area, stars(score), msg])
    return rows


