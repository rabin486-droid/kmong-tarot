# 한국어 조사 자동 분기 헬퍼
# ============================================================

def _has_jongseong(word):
    """단어 마지막 글자가 받침 있는지 확인."""
    if not word:
        return False
    last = word[-1]
    code = ord(last)
    if 0xAC00 <= code <= 0xD7A3:  # 한글 음절
        return (code - 0xAC00) % 28 != 0
    return False


def _josa(word, with_jong, without_jong):
    """받침 여부에 따라 조사 선택. with_jong=받침O, without_jong=받침X"""
    return with_jong if _has_jongseong(word) else without_jong


