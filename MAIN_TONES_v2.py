def get_main_tone(card_key, length='daily'):
    '''
    카드 키에서 메인 톤 메시지 반환 (정/역 + 길이 분기)
    
    Args:
        card_key (str): 예: 'major_19_upright', 'wands_5_reversed'
        length (str): 'daily' | 'weekly' | 'monthly'
    
    Returns:
        dict: {
            'keyword': str,    # 핵심 키워드 (예: '풍요')
            'main_msg': str,   # 길이에 맞게 조립된 메시지
            'core': str,       # p6 굵은 글씨용 한 줄
        }
    '''
    category = _get_category(card_key)
    direction = 'reversed' if card_key.endswith('_reversed') else 'upright'
    
    tone_data = MAIN_TONES.get(category, MAIN_TONES['시작/모험'])
    direction_data = tone_data.get(direction, tone_data['upright'])
    
    # 길이에 따라 조립
    parts = [direction_data['short']]
    if length in ('weekly', 'monthly'):
        parts.append(direction_data['extra1'])
    if length == 'monthly':
        parts.append(direction_data['extra2'])
    
    return {
        'keyword': direction_data['keyword'],
        'main_msg': ' '.join(parts),
        'core': direction_data['core'],
    }
