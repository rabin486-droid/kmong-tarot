def _date_seed(date_obj, salt=''):
    """날짜 + 솔트 → 정수 시드"""
    if hasattr(date_obj, 'strftime'):
        key = date_obj.strftime('%Y%m%d') + salt
    else:
        key = str(date_obj) + salt
    h = hashlib.md5(key.encode('utf-8')).hexdigest()
    return int(h[:8], 16)


def _pick(pool, date_obj, salt=''):
    """풀에서 날짜 시드로 1개 선택"""
    if not pool:
        return ''
    seed = _date_seed(date_obj, salt)
    return pool[seed % len(pool)]


def _pick_n(pool, n, date_obj, salt=''):
    """풀에서 날짜 시드로 n개 중복 없이 선택"""
    if not pool:
        return []
    if n >= len(pool):
        return list(pool)
    # 시드로 셔플 후 앞 n개
    indexed = list(enumerate(pool))
    indexed.sort(key=lambda x: _date_seed(date_obj, salt + str(x[0])))
    return [pool[i] for i, _ in indexed[:n]]


