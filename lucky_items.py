def _generate_lucky_numbers(element, date_obj, count):
    """
    오행 길수에서 시드 기반으로 count개 숫자 선택.
    같은 날짜는 항상 같은 숫자 → 재현 가능.
    """
    pool = LUCKY_NUMBERS_BY_ELEMENT.get(element, list(range(1, 100)))
    return _pick_n(pool, count, date_obj, salt='num')


