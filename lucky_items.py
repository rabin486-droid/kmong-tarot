# 메인 함수: 월간 20개 (주간 15개 + 추가 5개, 단 숫자는 6개)
# ============================================================

def get_monthly_items(profile, card_id, mbti, date_obj):
    """
    월간 행운 아이템 dict 반환.
    
    주간 항목 그대로 + 다음 5개 추가:
      추천 영상 장르 / 행운의 액세서리 / 오늘의 키워드 /
      이달 선물 / 이달 추천 공간 / 이달 목표 연결
    
    단, 행운의 숫자는 4개 → 6개로 확장.
    이달 목표 연결은 별도 키 (p7에서 사용, p5 표에는 안 들어감).
    """
    element = profile['ELEMENT']
    category = _get_category(card_id)
    
    # 숫자 6개로 확장
    nums = _generate_lucky_numbers(element, date_obj, 6)
    
    # 주간 항목 가져온 후 숫자만 덮어쓰기 + 5개 추가
    items = get_weekly_items(profile, card_id, date_obj)
    items['행운의 숫자1'] = nums[0] if len(nums) > 0 else ''
    items['행운의 숫자2'] = nums[1] if len(nums) > 1 else ''
    items['행운의 숫자3'] = nums[2] if len(nums) > 2 else ''
    items['행운의 숫자4'] = nums[3] if len(nums) > 3 else ''
    items['행운의 숫자5'] = nums[4] if len(nums) > 4 else ''
    items['행운의 숫자6'] = nums[5] if len(nums) > 5 else ''
    
    # 월간 추가 5개 (자연 아이템 삭제, 이달 선물·이달 추천 공간 추가)
    items['추천 영상 장르'] =  _pick(VIDEO_GENRES_BY_CATEGORY.get(category, []), date_obj, 'video')
    items['행운의 액세서리'] = _pick(ACCESSORIES, date_obj, 'acc')
    items['오늘의 키워드'] =   _pick(KEYWORDS_BY_CATEGORY.get(category, []), date_obj, 'kw')
    items['이달 선물'] =       _pick(GIFTS_BY_CATEGORY.get(category, []), date_obj, 'gift')
    items['이달 추천 공간'] =  _pick(SPACES_BY_CATEGORY.get(category, []), date_obj, 'space')
    
    # 이달 목표 연결 — MBTI + 카드 카테고리 자동 생성
    strength = MBTI_STRENGTHS.get(mbti, '본인만의 강점')
    keyword = _pick(KEYWORDS_BY_CATEGORY.get(category, ['균형']), date_obj, 'kw')
    # 한글 받침 여부에 따라 을/를 자동 분기
    last_char = strength[-1] if strength else ''
    has_jongseong = (ord(last_char) - 0xAC00) % 28 != 0 if 0xAC00 <= ord(last_char) <= 0xD7A3 else False
    josa = '을' if has_jongseong else '를'
    items['이달 목표 연결'] = f"{strength}{josa} 살려 '{keyword}'에 집중하는 한 달"
    
    return items


