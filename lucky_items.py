# 메인 함수: 주간 15개 (일간 10개 + 추가 5개, 단 숫자는 4개)
# ============================================================

def get_weekly_items(profile, card_id, date_obj):
    """
    주간 행운 아이템 dict 반환.
    
    일간 항목 그대로 + 다음 5개 추가:
      추천 차 / 행운의 꽃 / 피해야 할 것 / 오늘의 행운 물건 / 행운의 복권
    
    단, 행운의 숫자는 2개 → 4개로 확장.
    """
    element = profile['ELEMENT']
    category = _get_category(card_id)
    
    # 숫자 4개로 확장
    nums = _generate_lucky_numbers(element, date_obj, 4)
    
    # 일간 항목 가져온 후 숫자만 덮어쓰기 + 5개 추가
    items = get_daily_items(profile, card_id, date_obj)
    items['행운의 숫자1'] = nums[0] if len(nums) > 0 else ''
    items['행운의 숫자2'] = nums[1] if len(nums) > 1 else ''
    items['행운의 숫자3'] = nums[2] if len(nums) > 2 else ''
    items['행운의 숫자4'] = nums[3] if len(nums) > 3 else ''
    
    # 주간 추가 5개
    items['추천 차'] =         _pick(TEAS_BY_ELEMENT.get(element, []), date_obj, 'tea')
    items['행운의 꽃'] =       _pick(FLOWERS_BY_ELEMENT.get(element, []), date_obj, 'flower')
    items['피해야 할 것'] =    _pick(AVOID_BY_CATEGORY.get(category, []), date_obj, 'avoid')
    items['오늘의 행운 물건'] = _pick(LUCKY_OBJECTS_BY_CATEGORY.get(category, []), date_obj, 'object')
    # 복권은 주차 단위 (한 주 내내 같은 복권 추천)
    week_obj = date_obj - timedelta(days=date_obj.weekday())  # 주의 월요일로 정규화
    items['행운의 복권'] =     _pick(LOTTERIES, week_obj, 'lotto')
    
    return items


