# 메인 함수: 일간 10개
# ============================================================

def get_daily_items(profile, card_id, date_obj):
    """
    일간 행운 아이템 10개 dict 반환.
    
    [입력]
      profile: manse_calc.calc_profile() 결과 dict
      card_id: 그날 뽑힌 카드 ID (예: 'major_00_upright')
      date_obj: 날짜 (datetime.date)
    
    [출력]
      OrderedDict 형태로 PDF 표 순서 그대로:
        행운의 색 / 행운의 음식 / 귀인 조우 / 행운의 숫자1 / 행운의 숫자2 /
        행운의 방향 / 추천 향기 / 추천 소재 / 추천 음악 / 황금 시간대 / 행운의 원석
    """
    element = profile['ELEMENT']
    category = _get_category(card_id)
    
    nums = _generate_lucky_numbers(element, date_obj, 2)
    
    items = {
        '행운의 색':     _pick(COLORS_BY_ELEMENT.get(element, []), date_obj, 'color'),
        '행운의 음식':   _pick(FOODS_BY_ELEMENT.get(element, []), date_obj, 'food'),
        '귀인 조우':     _pick(GUIN_BY_CATEGORY.get(category, []), date_obj, 'guin'),
        '행운의 숫자1':  nums[0] if len(nums) > 0 else '',
        '행운의 숫자2':  nums[1] if len(nums) > 1 else '',
        '행운의 방향':   _pick(DIRECTIONS_BY_ELEMENT.get(element, []), date_obj, 'dir'),
        '추천 향기':     _pick(SCENTS_BY_ELEMENT.get(element, []), date_obj, 'scent'),
        '추천 소재':     _pick(MATERIALS_BY_ELEMENT.get(element, []), date_obj, 'mat'),
        '추천 음악':     _pick(MUSIC_BY_CATEGORY.get(category, []), date_obj, 'music'),
        '황금 시간대':   _pick(GOLDEN_HOURS, date_obj, 'golden'),
        '행운의 원석':   _pick(GEMS_BY_ELEMENT.get(element, []), date_obj, 'gem'),
    }
    return items


