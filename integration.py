def _get_category(card_key):
    """card_key → 카테고리"""
    base = card_key.replace('_upright', '').replace('_reversed', '')
    return CARD_CATEGORIES.get(base, '안정/풍요')


def get_main_tone(card_key, length='daily'):
    """
    카드의 메인 톤 반환 — p1, p6에서 사용.
    
    정/역방향 분기 + length별 텍스트 조립:
      length='daily'   → short
      length='weekly'  → short + extra1
      length='monthly' → short + extra1 + extra2
    
    반환 키: keyword / main_msg / core  (기존과 동일)
    """
    category = _get_category(card_key)
    direction = 'reversed' if card_key.endswith('_reversed') else 'upright'

    tone_data = MAIN_TONES.get(category, MAIN_TONES['안정/풍요'])
    direction_data = tone_data.get(direction, tone_data['upright'])

    parts = [direction_data['short']]
    if length in ('weekly', 'monthly'):
        parts.append(direction_data['extra1'])
    if length == 'monthly':
        parts.append(direction_data['extra2'])

    return {
        'keyword':  direction_data['keyword'],
        'main_msg': ' '.join(parts),
        'core':     direction_data['core'],
    }


def get_element_support(card_key, element):
    """오행이 메인 톤에 기여하는 방식"""
    category = _get_category(card_key)
    cat_map = ELEMENT_SUPPORT.get(category, ELEMENT_SUPPORT['안정/풍요'])
    return cat_map.get(element, f"{element}의 기운이 흐름을 받쳐줍니다.")


