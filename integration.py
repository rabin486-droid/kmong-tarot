def _has_jongseong(word):
    """단어 마지막 글자가 받침 있는지 확인."""
    if not word:
        return False
    last = word[-1]
    code = ord(last)
    if 0xAC00 <= code <= 0xD7A3:  # 한글 음절
        return (code - 0xAC00) % 28 != 0
    return False


def josa_i(word):
    """이/가 자동 선택"""
    return '이' if _has_jongseong(word) else '가'


def josa_eun(word):
    """은/는 자동 선택"""
    return '은' if _has_jongseong(word) else '는'


def josa_eul(word):
    """을/를 자동 선택"""
    return '을' if _has_jongseong(word) else '를'


def josa_wa(word):
    """과/와 자동 선택"""
    return '과' if _has_jongseong(word) else '와'


def josa_euro(word):
    """으로/로 자동 선택 (받침이 'ㄹ'이면 '로')"""
    if not word:
        return '로'
    last = word[-1]
    code = ord(last)
    if 0xAC00 <= code <= 0xD7A3:
        jong = (code - 0xAC00) % 28
        if jong == 0:
            return '로'
        elif jong == 8:  # ㄹ
            return '로'
        else:
            return '으로'
    return '로'


def josa_ira(word):
    """이라는/라는 자동 선택"""
    return '이라는' if _has_jongseong(word) else '라는'


def josa_iya(word):
    """이야/야 자동 선택"""
    return '이야' if _has_jongseong(word) else '야'


